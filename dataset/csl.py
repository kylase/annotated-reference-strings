from enum import Enum, unique


@unique
class Variable(Enum):
    """
    An enumeration of `CSL 1.0.1 variables <https://docs.citationstyles.org/en/stable/specification.html#appendix-iv-variables>`_.

    .. NOTE::
        *other* is the only non-standard value and its intention is to assign to tokens that are outside the segments enclosed by the tags
    """

    ABSTRACT = "abstract"
    ANNOTE = "annote"
    ARCHIVE = "archive"
    ARCHIVE_LOCATION = "archive_location"
    ARCHIVE_PLACE = "archive-place"
    AUTHORITY = "authority"
    CALL_NUMBER = "call-number"
    CITATION_LABEL = "citation-label"
    CITATION_NUMBER = "citation-number"
    COLLECTION_TITLE = "collection-title"
    CONTAINER_TITLE = "container-title"
    CONTAINER_TITLE_SHORT = "container-title-short"
    DIMENSIONS = "dimensions"
    DOI = "DOI"
    EVENT = "event"
    EVENT_PLACE = "event-place"
    FIRST_REFERENCE_NOTE_NUMBER = "first-reference-note-number"
    GENRE = "genre"
    ISBN = "ISBN"
    ISSN = "ISSN"
    JURISDICTION = "jurisdiction"
    KEYWORD = "keyword"
    LOCATOR = "locator"
    MEDIUM = "medium"
    NOTE = "note"
    ORIGINAL_PUBLISHER = "original-publisher"
    ORIGINAL_PUBLISHER_PLACE = "original-publisher-place"
    ORIGINAL_TITLE = "original-title"
    PAGE = "page"
    PAGE_FIRST = "page-first"
    PMCID = "PMCID"
    PMID = "PMID"
    PUBLISHER = "publisher"
    PUBLISHER_PLACE = "publisher-place"
    REFERENCES = "references"
    REVIEWED_TITLE = "reviewed-title"
    SCALE = "scale"
    SECTION = "section"
    SOURCE = "source"
    STATUS = "status"
    TITLE = "title"
    TITLE_SHORT = "title-short"
    URL = "URL"
    VERSION = "version"
    YEAR_SUFFIX = "year-suffix"
    # Number Variable
    CHAPTER_NUMBER = "chapter-number"
    COLLECTION_NUMBER = "collection-number"
    EDITION = "edition"
    ISSUE = "issue"
    NUMBER = "number"
    NUMBER_OF_PAGES = "number-of-pages"
    NUMBER_OF_VOLUMES = "number-of-volumes"
    VOLUME = "volume"
    # Date Variable
    ACCESSED = "accessed"
    CONTAINER = "container"
    EVENT_DATE = "event-date"
    ISSUED = "issued"
    ORIGINAL_DATE = "original-date"
    SUBMITTED = "submitted"
    # Name Variable
    AUTHOR = "author"
    COLLECTION_EDITOR = "collection-editor"
    COMPOSER = "composer"
    CONTAINER_AUTHOR = "container-author"
    DIRECTOR = "director"
    EDITOR = "editor"
    EDITORIAL_DIRECTOR = "editorial-director"
    ILLUSTRATOR = "illustrator"
    INTERVIEWER = "interviewer"
    ORIGINAL_AUTHOR = "original-author"
    RECIPIENT = "recipient"
    REVIEWED_AUTHOR = "reviewed-author"
    TRANSLATOR = "translator"
    # These are components of the Date Variable
    YEAR = "year"
    MONTH = "month"
    DAY = "day"
    # Not included
    OTHER = "other"
